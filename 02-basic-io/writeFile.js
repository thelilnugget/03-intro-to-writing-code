const fs = require('fs');
const path = require('path');

//----------------------------------------------------------------------------------------------------------------------

function writeFile(location, contents)
{
    fs.writeFileSync(path.resolve(__dirname, '..', location), contents);
}

//----------------------------------------------------------------------------------------------------------------------

writeFile('./test.txt', 'This is my file. There are many like it, but this one is mine.');

